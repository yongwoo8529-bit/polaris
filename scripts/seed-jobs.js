function fetchJson(url) {
  return new Promise((resolve, reject) => {
    https.get(url, (res) => {
      let data = ''
      res.on('data', chunk => data += chunk)
      res.on('end', () => {
        try { resolve(JSON.parse(data)) } catch (e) { reject(e) }
      })
      res.on('error', reject)
    })
  })
}

async function fetchAllCareerNetJobs() {
  const perPage = 100
  let allJobs = []
  let pageIndex = 1
  process.stderr.write('커리어넷 직업 목록 수집 중...\n')
  while (true) {
    const url = `https://www.career.go.kr/cnet/openapi/getOpenApi?apiKey=${CAREER_NET_KEY}&svcType=api&svcCode=JOB&contentType=json&gubun=job_dic_list&perPage=${perPage}&pageIndex=${pageIndex}`
    const data = await fetchJson(url)
    const content = data?.dataSearch?.content
    if (!content || content.length === 0) break
    allJobs = allJobs.concat(content)
    const total = parseInt(content[0]?.totalCount || '0')
    process.stderr.write(`  ${allJobs.length}/${total}\n`)
    if (allJobs.length >= total) break
    pageIndex++
  }
  return allJobs
}

function normalize(str) {
  return str.replace(/[·\-\s,\.()（）\[\]]/g, '').toLowerCase()
}

function findCareerNetMatch(jobName, careerNetJobs) {
  // 0. 별칭 우선 적용
  const alias = CAREER_NET_ALIASES[jobName]
  if (alias) {
    const aliasMatch = careerNetJobs.find(j => normalize(j.job) === normalize(alias))
    if (aliasMatch) return aliasMatch
  }
  const normTarget = normalize(jobName)
  // 1. 정규화 완전 일치
  const exact = careerNetJobs.find(j => normalize(j.job) === normTarget)
  if (exact) return exact
  // 2. 괄호 앞 기본 이름으로 일치
  const base = normalize(jobName.split('(')[0].split('·')[0].trim())
  if (base.length > 2) {
    const partial = careerNetJobs.find(j => normalize(j.job) === base)
    if (partial) return partial
    // 3. 포함 관계
    const contained = careerNetJobs.find(j =>
      normalize(j.job).includes(base) || base.includes(normalize(j.job)) && normalize(j.job).length > 3
    )
    if (contained) return contained
  }
  return null
}

function esc(str) {
  return (str || '').replace(/'/g, "''")
}

async function main() {
  const careerNetJobs = await fetchAllCareerNetJobs()
  process.stderr.write(`총 ${careerNetJobs.length}개 수집 완료\n\n`)

  const lines = [
    '-- POLARIS 직업 데이터 삽입 스크립트 (커리어넷 API 자동 생성)',
    `-- 생성일: ${new Date().toISOString()}`,
    '',
    'ALTER TABLE jobs ADD COLUMN IF NOT EXISTS career_net_seq TEXT;',
    '',
    'INSERT INTO jobs (name, galaxy_id, description, salary, career_net_seq)',
    'VALUES',
  ]

  const values = []
  let matchCount = 0

  for (const job of GALAXY_JOBS) {
    const match = findCareerNetMatch(job.name, careerNetJobs)
    const fallback = JOB_DATA[job.name] || {}

    // 설명: 커리어넷 summary 우선, 없으면 직접 작성
    const rawDesc = match?.summary
      ? match.summary.replace(/\n/g, ' ').replace(/\s+/g, ' ').trim().substring(0, 150)
      : (fallback.desc || '')

    // 연봉: 커리어넷 salery 우선, 없으면 직접 작성
    const salary = match?.salery || fallback.salary || ''
    const seq = match?.jobdicSeq || ''

    if (match) {
      matchCount++
      process.stderr.write(`✓ "${job.name}" → "${match.job}"\n`)
    } else {
      process.stderr.write(`✗ "${job.name}" (폴백)\n`)
    }

    values.push(`('${esc(job.name)}', ${job.galaxy_id}, '${esc(rawDesc)}', '${esc(salary)}', '${seq}')`)
  }

  lines.push(values.join(',\n') + ';')
  process.stderr.write(`\n매칭 성공: ${matchCount}/${GALAXY_JOBS.length}\n`)
  console.log(lines.join('\n'))
}

main().catch(err => {
  process.stderr.write(`오류: ${err.message}\n`)
  process.exit(1)
})
